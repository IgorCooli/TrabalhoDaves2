package Investidores;

public class LeiIncentivo {
	private String nomeLei;
	private double valorConcedido;
	
	public LeiIncentivo() {
		super();
	}

	public String getNomeLei() {
		return nomeLei;
	}

	public void setNomeLei(String nomeLei) {
		this.nomeLei = nomeLei;
	}

	public double getValorConcedido() {
		return valorConcedido;
	}

	public void setValorConcedido(double valorConcedido) {
		this.valorConcedido = valorConcedido;
	}
	
	
	
}
