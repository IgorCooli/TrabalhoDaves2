package Cenas;

public class Fita {
	
	private Cena cena;
	private double capacidade;
	
	public Fita() {}

	public Cena getCena() {
		return cena;
	}

	public void setCena(Cena cena) {
		this.cena = cena;
	}

	public double getCapacidade() {
		return capacidade;
	}

	public void setCapacidade(double capacidade) {
		this.capacidade = capacidade;
	}


	
	
	
}
